// This line includes the standard input/output library
// It's necessary for using functions like printf
#include <stdio.h>

// This is the main function, the entry point of every C program
// It returns an integer (int) and takes no arguments (empty parentheses)
int main() {
    // This line prints "Hello, World!" to the console
    // printf is a function from stdio.h that outputs formatted text
    // \n is an escape sequence that adds a newline at the end
    printf("Hello, World!\n");

    // This line returns 0 to indicate successful program execution
    // In C, returning 0 from main() is a convention meaning "no errors"
    return 0;
}
